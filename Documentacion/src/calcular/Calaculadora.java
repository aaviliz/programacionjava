public class Calaculadora {

	/**
	 * <h2>Metodos para comprobar si un numero es par<h2>
	 * @param a
	 * @return
	 */
	public boolean esPar(int a) {
		return a % 2 == 0;
	}

	/**
	 * 
	 * @param a
	 * @param b
	 * @return
	 */
	public int suma(int a, int b){
		return a+ b;
	}
}
